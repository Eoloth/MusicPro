    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        
        // consumir servicio
        Categoria sw = new Categoria();
        ICategoria swCategoria = sw.getBasicHttpBindingICategoria();
        org.datacontract.schemas._2004._07.capadenegocio.ArrayOfCategoria listado = swCategoria.listar();
        
        // llenar la tabla con los datos entregados por el servicio
        DefaultTableModel modelo = (DefaultTableModel) tblDatos.getModel();
        
        //elimina las filas de la tabla
        modelo.setRowCount(0);
        
        // extrae los datos y muestra en tabla
        for(org.datacontract.schemas._2004._07.capadenegocio.Categoria c: listado.getCategoria())
        {
            modelo.addRow(new Object[]{""+c.getId(), c.getNombre().getValue(), c.getActivo()==1 });
        }
        
    }//GEN-LAST:event_btnListarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
      // captura de los datos
      int id= 0;
      id = Integer.parseInt(txtId.getText());
      
      if(id > 0)
      {
          // crear SW
        Categoria sw = new Categoria();
        ICategoria swCategoria = sw.getBasicHttpBindingICategoria();
         
         // consumir m√©todo
        org.datacontract.schemas._2004._07.capadenegocio.Categoria categoria = swCategoria.read(id);
        
        txtNombre.setText(categoria.getNombre().getValue());
        chkActivo.setSelected(categoria.getActivo()==1);
        
      }
      else
          JOptionPane.showMessageDialog(this, "Id no encontrado");
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int id = 0;
        int resultado = 0;
        
        id = Integer.parseInt(txtId.getText());
                // crear SW
        Categoria sw = new Categoria();
        ICategoria swCategoria = sw.getBasicHttpBindingICategoria();
         
        // consumir m√©todo
        resultado = swCategoria.delete(id);
        
      if(resultado == 1)
          JOptionPane.showMessageDialog(this, "Datos Eliminados");
      else
          JOptionPane.showMessageDialog(this, "Datos No Eliminados");
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:  
        int id = 0;
        String nombre = "";
        int activo = 0;
        int resultado = 0;
        
        id = Integer.parseInt(txtId.getText());
        nombre = txtNombre.getText();
        activo = chkActivo.isSelected()?1:0;
      
        // crear SW
        Categoria sw = new Categoria();
        ICategoria swCategoria = sw.getBasicHttpBindingICategoria();
         
        // consumir m√©todo
        resultado = swCategoria.update(id,nombre, activo);
        
      if(resultado == 1)
          JOptionPane.showMessageDialog(this, "Datos Modificados");
      else
          JOptionPane.showMessageDialog(this, "Datos No Modificados");
    }//GEN-LAST:event_btnModificarActionPerformed


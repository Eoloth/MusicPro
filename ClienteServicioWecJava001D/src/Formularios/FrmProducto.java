    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        // rescatar los datos del formulario
        int id = 0;
        id = Integer.parseInt(txtId.getText());
        
        Producto sw = new Producto();
        IProducto swProducto = sw.getBasicHttpBindingIProducto();
        org.datacontract.schemas._2004._07.capadenegocio.Producto prod = swProducto.read(id);
        
        if(prod == null)
        {
            JOptionPane.showMessageDialog(this, "El codigo del producto no existe");
        }
        else
        {
            txtCodigoBarra.setText(prod.getCodigoBarra().toString());
            txtDescripcion.setText(prod.getDescripcion().getValue());
            txtPrecioCosto.setText("" + prod.getPrecioCosto());
            txtPrecioVenta.setText("" + prod.getPrecioVenta());
            txtStock.setText("" + prod.getStock());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


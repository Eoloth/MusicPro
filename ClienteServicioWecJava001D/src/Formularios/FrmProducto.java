    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        // TODO add your handling code here:
        Producto sw = new Producto();
        IProducto swProducto = sw.getBasicHttpBindingIProducto();
        // rescatar los datos del formulario
        int codigo = 0;
        String descripcion = "";
        int pc = 0;
        int pv = 0;
        int stock = 0;
        
        codigo = Integer.parseInt(txtCodigoBarra.getText());
        descripcion = txtDescripcion.getText();
        pc = Integer.parseInt(txtPrecioCosto.getText());
        pv = Integer.parseInt(txtPrecioVenta.getText());
        stock = Integer.parseInt(txtStock.getText());
        
        int resultado = swProducto.create(codigo, descripcion, pc, pv, stock);
        
        if(resultado == 1)
        {
            JOptionPane.showMessageDialog(this, "Datos fueron guardados");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Datos NO fueron guardados");
        }
        
        
    }//GEN-LAST:event_btnGrabarActionPerformed

